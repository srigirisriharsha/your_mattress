export function generateRandomId(arg1, arg2) {
  const characters =
    'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
  let randomPart = '';

  // Generate random part of the ID with 25 characters length
  for (let i = 0; i < 25; i++) {
    randomPart += characters.charAt(
      Math.floor(Math.random() * characters.length)
    );
  }

  // Add the two arguments in front of the random ID
  const finalId = `${arg1}${arg2}${randomPart}`;
  return finalId;
}
