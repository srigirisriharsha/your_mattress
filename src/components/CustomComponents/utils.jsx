export const FirstLetters = (name) => {
  let str = name?.trim(); // Trim the input to remove leading/trailing spaces

  let firstName = str?.split(' ').slice(0, -1).join(' ');
  let lastName = str?.split(' ').slice(-1).join(' ');

  let displayLetters = '';

  if (firstName) {
    displayLetters += firstName[0];
  }

  if (lastName) {
    displayLetters += lastName[0];
  }

  return displayLetters.toUpperCase();
};
